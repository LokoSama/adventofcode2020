const list = stringList.split("\n").map(line => line.split('')) // [line][column]

// PART 1
function computeWay(lMove, cMove){
    let trees = 0
    let lIndex = 0
    let cIndex = 0
    while (lIndex < list.length) {
        if (list[lIndex][cIndex] === '#'){
            trees++
        }
        lIndex += lMove
        cIndex += cMove
        cIndex = cIndex % (list[0].length)
    }
    return trees
}
console.log("PART1 RESULT: " + computeWay(1,3))

//PART 2
const configs = [[1,1],[3,1],[5,1],[7,1],[1,2]]
let result = 1
for (const config of configs){
    result *= computeWay(config[1], config[0])
}
console.log("PART2 RESULT: " + result)
